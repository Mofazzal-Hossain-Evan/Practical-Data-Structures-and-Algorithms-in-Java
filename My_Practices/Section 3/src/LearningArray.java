public class LearningArray {
    public static void main(String[] args) {

        double[] values = new double[100];
        values[0] = 100;
        values[1] = 232.3;
        values[88] = 345434;

        System.out.println(values[0]);

        String[] words = new String[3];
        words[0] = "ty";
        words[1] = "bow";
        words[2] = "wo";
        System.out.println(words[0]);
        words = new String[10];
        System.out.println(words[0]);

    }
}
